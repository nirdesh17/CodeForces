#include<bits/stdc++.h>
using namespace std;
#define ll long long int
  
int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    ll t;
    cin>>t;
    for(ll i=0;i<t;i++)
    {
        ll a,b;
        cin>>a>>b;
        for(ll j=0;j<a;j++)
        {
            cout<<0;
        }
        for(ll j=0;j<b;j++)
        {
            cout<<1;
        }
        cout<<endl;
    }
    return 0;
}